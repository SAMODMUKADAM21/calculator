function add(number1, number2) {
  return number1 + number2;
}

const number1 = parseInt(prompt("Enter a num"));
const number2 = parseInt(prompt("Enter another num"));
const result = add(number1, number2);
alert(result);

// Subtraction
function subtract(num1,num2) {
  return num1 - num2;
}

const number3 = parseInt(prompt("Enter a num"));
const number4 = parseInt(prompt("Enter another num"));
const subtractResult = subtract(number3, number4);
alert(subtractResult);
